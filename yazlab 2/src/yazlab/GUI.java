    private void kullaniciSecimİtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_kullaniciSecimİtemStateChanged
        // TODO add your handling code here:
        
        if(String.valueOf(kullaniciSecim.getSelectedItem())=="TAŞ"){
            kullanici.kullaniciSecim = 0;
            kullanici.nesneSec();   
            kResimButon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resimler/tas.png")));
        }else if(String.valueOf(kullaniciSecim.getSelectedItem())=="KAĞIT"){
            kullanici.kullaniciSecim = 1;
            kullanici.nesneSec();
            kResimButon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resimler/kagit.png")));
        
        }else if(String.valueOf(kullaniciSecim.getSelectedItem())=="MAKAS"){
            kullanici.kullaniciSecim = 2;
            kullanici.nesneSec();
            kResimButon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resimler/makas.png")));
        }
    }//GEN-LAST:event_kullaniciSecimİtemStateChanged

